    <h1>Biblioteca</h1><br>
    <p class="citation">"O único coisa que você absolutamente tem que saber, é a localização de uma biblioteca." - Albert Einstein</p>

    <p class="justified">Esta citação de Einstein realça a biblioteca como um recurso fundamental para o aprendizado e a descoberta. Ele sugere que, independentemente do que se deseja aprender ou descobrir, a biblioteca é um ponto de partida essencial, um lugar onde a busca pelo conhecimento pode começar. Bibliotecas oferecem acesso a uma vasta gama de informações e conhecimento acumulado ao longo dos séculos, disponibilizando-o a todos que desejam explorar, aprender e crescer.</p>

    <p class="justified">Uma biblioteca é uma coleção organizada de fontes de informação, seja em formato físico (como livros, revistas, jornais, manuscritos, filmes, e mapas) ou digital (e-books, acesso a bases de dados, revistas e jornais eletrônicos, etc.). Elas são projetadas para atender às necessidades de informação, pesquisa, estudo, entretenimento e promoção do conhecimento de seus usuários. Além disso, as bibliotecas oferecem serviços como o empréstimo de materiais, espaços para estudo e leitura, e atividades culturais e educacionais.</p>
